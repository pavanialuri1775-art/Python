#Assignment operators:Assignment operators are used to assign values to variables
#Simple assignment
x = 10
print(x)

#Assign multiple values
a = 5
b = a
print(a, b)

#ADDITION ASSIGNMENT (+=)
#*Add and assign
x = 10
