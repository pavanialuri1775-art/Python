#square pattern
for i in range(1,6):
    print("*"*5)
    
#number increasing pattern
n=5
for i in range(1,n+1):
    for j in range(1,i+1):
        print(j,end="")
    print()
    
#Right triangle star pattern
n=5
for i in range(1,n+1):
    for j in range(1,i+1):
        print("*",end="")
    print()
    

    
    
        
        
        
        
        
        
        
        
        
        
        
    