#AND operator:set to 1 if both the binary bits are 1 else 0
a=5
b=3
print(a&b)  #1
#OR operator:set to one if one of the two bits is 1 else 0
print(5 | 3) #7

#XOR operator:set to 1 if only 1 bit is 1 among two
print(5 ^ 3) #6

#NOT:Inverts all the bits
print(~5) # -(5+1)=6

#left shift
print(10 << 1)


